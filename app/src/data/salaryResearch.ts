/**
 * Get salary range for a specific country and major.
 * Falls back to country average if major not found.
 */
export function getMajorSalary(country: string, major: string): SalaryRange {
  const countryData = SALARY_BY_MAJOR[country];
  if (!countryData) return { entry: 25, mid: 40, senior: 65 }; // global fallback
  return countryData[major] || { entry: 25, mid: 40, senior: 65 };
}

